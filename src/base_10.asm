#import "gfx.asm"


screen_col: .byte 0 
screen_row: .byte 0

display_number:
    lda number_ptr: $FFFF
    pha
    
    tax
    lda number_hi, x
    ldx screen_col
    ldy screen_row
    jsr put_char

    inx

    pla
    pha
    tax
    lda number_mid, x
    ldx screen_col
    inx
    ldy screen_row
    jsr put_char

    pla
    tax
    lda number_lo, x
    ldx screen_col
    inx
    inx
    ldy screen_row
    jsr put_char
    rts

.macro setup_base10_disp(target_number, pos_x, pos_y) {
    lda #pos_x
    sta screen_col
    lda #pos_y
    sta screen_row
    lda #<target_number
    sta number_ptr
    lda #>target_number
    sta number_ptr+1
}

